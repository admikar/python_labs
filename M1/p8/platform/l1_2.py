# Создаем список строк
strings = ["apple", "banana", "cherry", "date", "fig", "grape"]

# Используем sorted() и лямбда-функцию для сортировки строк по длине
sorted_strings = sorted(strings, key=lambda x: len(x))

# Выводим результат
print(sorted_strings)
